package leetcode;

public class FindWord1160 {


    public static void main(String[] args) {

        int i = countCharacters(new String[]{"1", "2"}, "12");
        System.out.println("212");
    }
    private static  int countCharacters(String[] words, String chars){
        for (String s:words){

            chars.contains(s);
        }


        return 1;
    }

}
